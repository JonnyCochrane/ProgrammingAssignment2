## Functions for caching the inverse of a square matrix

## makeCacheMatrix creates a special matrix object than can cache its inverse

makeCacheMatrix <- function(x = matrix()) {
    m <- NULL
    set <- function(y) {
        x <<- y
        m <<- NULL
    }
    get <- function() x
    setinverse <- function(solve) m <<- solve
    getinverse <- function() m
    list(set = set, get = get,
         setinverse = setinverse,
         getinverse = getinverse)

}


## cacheSolve calculates the inverse of a matrix 'x' created using the
## makeCacheMatrix() function

cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'z'
    m <- x$getinverse()
    if(!is.null(m)) {
        message("getting cached data")
        return(m)
    }
    data <- x$get()
    m <- solve(data, ...)
    x$setinverse(m)
    m
}
